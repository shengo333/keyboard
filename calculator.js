
function sum(){
    var input1 = document.getElementById("input1").value;
    var input2 = document.getElementById("input2").value;
    var output = document.getElementById("output");
    output.innerHTML = parseInt(input1) + parseInt(input2);

    console.log(input1)
    console.log(input2)
    console.log(output)

}
